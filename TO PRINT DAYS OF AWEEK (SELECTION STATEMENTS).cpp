#include<stdio.h>
 main()
{
	int days;
	scanf("%d",&days);
	switch(days)
	{
	  case 1: printf("sun");
	           break;
	  case 2: printf("mon");
	          break;
	  case 3: printf("tue");
	          break; 
	  case 4: printf("wed");
	           break;
	  case 5: printf("thurs");
	          break;
	  case 6: printf("fri");
	           break;
	  case 7: printf("sat");
	           break;		   		         
				
		     
	}
		  
} 
